package LZY.Day20191121_JavaBase.DataType;

/**
 * @Title: Java8大基本数据类型
 */
public class DataTypeBasic {
    /**
     * @byte：8位
     * @short：16位
     * @int：32位
     * @long：64位
     * @float：32位
     * @double：64位
     * @boolean：1位
     * @char：16位
     */
    static byte a;
    static short b;
    static int c;
    static long d;
    static float e;
    static double f;
    static boolean g;
    static char h;
    public static void main(String[] args) {
        System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g + " " + h);
    }
}
